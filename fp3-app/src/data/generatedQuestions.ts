function makeAcademicQuestions(): Question[] {
  return Object.entries(academicFacts).flatMap(([category, facts]) =>
    facts.map((fact, index) => ({
      id: `auto-${slug(category as Category)}-${String(index + 1).padStart(3, '0')}`,
      category: category as Category,
      examType: '学科' as const,
      question: `${fact.term}に関する説明として、最も適切なものはどれか。`,
      choices: [fact.correct, ...fact.wrong],
      answerIndex: 0,
      explanation: `${fact.explanation} 暗記ポイント: ${fact.term}は「${fact.correct}」と押さえる。`,
    })),
  );
}

