function slug(category: Category) {
  const map: Record<Category, string> = {
    ライフプランニング: 'life',
    リスク管理: 'risk',
    金融資産運用: 'finance',
    タックスプランニング: 'tax',
    不動産: 'real',
    '相続・事業承継': 'inherit',
  };
  return map[category];
}

